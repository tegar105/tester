void main(){

  /// Hasil Perbandingan akan selalu menjadi tipe data boolean
  print(10 == 10);  /// == artinya sama dengan
  print (10 != 10); /// != artinya tidak sama dengan
  print(10 > 2);
  print(10 < 2);
  print(10 >= 11); /// >= artinya lebih besar atau sama dengan
  print(10 <= 11); /// <= artinya lebih kecil atau sama dengan

  print('tegar' == 'tegar');
  print('damar' != 'damar');
  

}