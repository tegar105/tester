void main() {

  // Jika kondisi True, maka akan terus looping sampai kondisinya False
  // Jika kondisi False, maka akan berhenti

  // counter++ artinya Isi dari Var counter dinaikkan 1
  //  init statement;    Kondisi;     Post Statement     <- Rumus For
  for(var counter = 1; counter <= 10; counter++){
    print("Semoga Lekas Sembuh");

  }

  // i +=3 artinya Var i dinaikkan 3
  for(var i = 3; i < 12; i +=3){
    print("Perulangan Ke- $i");
  }

  


}